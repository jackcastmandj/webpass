document.getElementById('login-form').addEventListener('submit', function (e) {
  e.preventDefault();

  const studentId = this.student_id.value.trim();
  const index = idToPageMap.indexOf(studentId);

  if (index !== -1) {
    const pageNumber = indexToPageNumber(index);
    window.location.href = `/nong/${pageNumber}.html`;
  } else {
    alert("❌ ไม่มีน้องรหัสนะจ๊ะ");
  }
});

function indexToPageNumber(index) {
  let count = 0;
  for (let i = 0; i <= index; i++) {
    if (idToPageMap[i] !== undefined) count++;
  }
  return count;
}
